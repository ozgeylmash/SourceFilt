CREATE TABLE `book` (
  `slug` VARCHAR(300) PRIMARY KEY,
  `name` VARCHAR(300) NOT NULL,
  `publisher` VARCHAR(100) NOT NULL,
  `number_of_page` INTEGER,
  `subject` ENUM ('sayısal', 'matematik', 'fen', 'fizik', 'kimya', 'biyoloji', 'sözel', 'türkçe', 'edebiyat', 'sosyal', 'tarih', 'coğrafya', 'felsefe', 'din', 'ingilizce', 'genel') NOT NULL,
  `grade` ENUM ('9.', '10.', '11.', '12.', 'tyt', 'ayt', 'ydt', 'lise') NOT NULL,
  `year` YEAR,
  `type` ENUM ('soru bankası', 'deneme', 'diğer') NOT NULL,
  `kitapsec_id` INTEGER,
  `islerkitap_id` INTEGER,
  `kitapyurdu_id` INTEGER,
  `bkmkitap_id` INTEGER,
  `isemkitap_id` INTEGER,
  `sadecekitap_id` INTEGER,
  `kitapsepeti_id` INTEGER,
  `created_at` TIMESTAMP DEFAULT (now()),
  FOREIGN KEY(kitapsec_id) REFERENCES kitapsec(id) ON DELETE SET NULL,
  FOREIGN KEY(islerkitap_id) REFERENCES islerkitap(id) ON DELETE SET NULL,
  FOREIGN KEY(kitapyurdu_id) REFERENCES kitapyurdu(id) ON DELETE SET NULL,
  FOREIGN KEY(bkmkitap_id) REFERENCES bkmkitap(id) ON DELETE SET NULL,
  FOREIGN KEY(isemkitap_id) REFERENCES isemkitap(id) ON DELETE SET NULL,
  FOREIGN KEY(sadecekitap_id) REFERENCES sadecekitap(id) ON DELETE SET NULL,
  FOREIGN KEY(kitapsepeti_id) REFERENCES kitapsepeti(id) ON DELETE SET NULL
);